// Get current language from localStorage or default to 'en'
export function getCurrentLanguage() {
    return localStorage.getItem('language') || 'en';
}

// Set language in localStorage
export function setLanguage(lang) {
    localStorage.setItem('language', lang);
    location.reload(); // Reload to apply language change
}

// Get translation for current language
export function t(key) {
    const lang = getCurrentLanguage();
    const keys = key.split('.');
    let value = translations[lang];

    for (const k of keys) {
        value = value[k];
        if (!value) return key;
    }

    return value;
}

// Render language toggle buttons
export function renderLanguageToggle() {
    const leftActions = document.querySelector('.header-actions:first-child');
    if (!leftActions) return;

    const currentLang = getCurrentLanguage();

    const czStyle = currentLang === 'cz'
        ? 'background:#f4f6f2;color:#0b6b3a;'
        : 'background:#2e8a57;color:#dff0e6;';

    const enStyle = currentLang === 'en'
        ? 'background:#f4f6f2;color:#0b6b3a;'
        : 'background:#2e8a57;color:#dff0e6;';

    leftActions.innerHTML = `
        <a href="#" id="lang-cz" style="text-decoration:none;font-family:'Courier New',monospace;font-size:11px;padding:2px 8px;border:1px solid #084d29;${czStyle}">CZ</a>
        <a href="#" id="lang-en" style="text-decoration:none;font-family:'Courier New',monospace;font-size:11px;padding:2px 8px;border:1px solid #084d29;${enStyle}">EN</a>
    `;

    document.getElementById('lang-cz').onclick = (e) => {
        e.preventDefault();
        setLanguage('cz');
    };

    document.getElementById('lang-en').onclick = (e) => {
        e.preventDefault();
        setLanguage('en');
    };
}

// Update all elements with data-i18n attribute
export function updateI18nElements() {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        el.textContent = t(key);
    });

    // Update placeholders for inputs with data-i18n-placeholder
    const placeholderElements = document.querySelectorAll('[data-i18n-placeholder]');
    placeholderElements.forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        el.placeholder = t(key);
    });
}
